package com.peterchege.cartify.presentation.theme

import androidx.compose.foundation.isSystemInDarkTheme
import androidx.compose.material.MaterialTheme
import androidx.compose.material.darkColors
import androidx.compose.material.lightColors
import androidx.compose.runtime.Composable
import androidx.compose.ui.graphics.Color

private val DarkColorPalette = darkColors(
    primary = Color.White,
    primaryVariant = WhiteColorVariant,

    background = BlueColor,
    onBackground = BlueColorVariant
)

private val LightColorPalette = lightColors(
    //text
    primary = Color.Black,
    primaryVariant = BlackColorVariant,

    //background
    background = Color.White,
    onBackground = WhiteColorVariant
)


@Composable
fun CartifyTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    val colors = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colors = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}